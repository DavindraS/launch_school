# The limit variable is out of scope of the fib function, so it has no idea
# what limit is referring to. To solve this, we can add a method parameter
# and pass in the limit as an argument.

limit = 15

def fib(first_num, second_num, limit)
  while first_num + second_num < limit
    sum = first_num + second_num
    first_num = second_num
    second_num = sum
  end
  sum
end

result = fib(0, 1, limit)
puts "result is #{result}"
